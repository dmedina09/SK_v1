oled_rotation_t oled_init_user(oled_rotation_t rotation) {
  if (!is_keyboard_master()) {
    return OLED_ROTATION_180;  // flips the display 180 degrees if offhand
  }
  return rotation;
}

#define L_BASE 0
#define L_LOWER 2
#define L_RAISE 4
#define L_ADJUST 8

void oled_render_layer_state(void) {
    oled_write_P(PSTR("Layer: "), false);
    switch (layer_state) {
        case L_BASE:
            oled_write_ln_P(PSTR("DEFAULT [0]"), false);
            break;
        case L_LOWER:
            oled_write_ln_P(PSTR("LOWER [1]"), false);
            break;
        case L_RAISE:
            oled_write_ln_P(PSTR("RAISE [2]"), false);
            break;
        case L_ADJUST:
        case L_ADJUST|L_LOWER:
        case L_ADJUST|L_RAISE:
        case L_ADJUST|L_LOWER|L_RAISE:
            oled_write_ln_P(PSTR("ADJUST [3]"), false);
            break;
    }
    oled_set_cursor(0, 1);
    oled_write("Model: SK v1 - 2021", false);
    oled_set_cursor(0, 2);
    oled_write("User: Diego M.", false);
    oled_set_cursor(0, 3);
    oled_write("Master: Left", false);
}

